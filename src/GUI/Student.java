    private void submit_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submit_ButtonActionPerformed
        // TODO add your handling code here:
        String rollNo = rollno_TextField.getText();
        int year = Integer.parseInt(year_TextField.getText());
        int sem = Integer.parseInt(sem_TextField.getText());
        try{
            Connection con = ConnectionProviderCode.getcon();
            Statement st = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
            ResultSet rs = null ;
            if(year ==1 && sem ==1){
                rs = st.executeQuery("SELECT * FROM firstresult WHERE rollNo='"+rollNo+"' AND year='"+1+"' AND semester='"+1+"'");
            }
            else if(year ==1 && sem ==2){
                rs = st.executeQuery("SELECT * FROM secondresult WHERE rollNo='"+rollNo+"' AND year='"+1+"' AND semester='"+2+"'");
            }
            else if(year ==2 && sem ==1 ){
                rs = st.executeQuery("SELECT * FROM cethirdresult WHERE rollNo='"+rollNo+"' AND year='"+2+"' AND semester='"+1+"'");
            }
            else if(year ==2 && sem ==2 ){
                rs = st.executeQuery("SELECT * FROM cefourthresult WHERE rollNo='"+rollNo+"' AND year='"+2+"' AND semester='"+2+"'");
            }
            else if(year ==3 && sem == 1){
                rs = st.executeQuery("SELECT * FROM cefifthresult WHERE rollNo='"+rollNo+"' AND year='"+3+"' AND semester='"+1+"'");
            }
            else if(year ==3 && sem == 2){
                rs = st.executeQuery("SELECT * FROM cesixthresult WHERE rollNo='"+rollNo+"' AND year='"+3+"' AND semester='"+2+"'");
            }
            else if(year ==4 && sem == 1){
                rs = st.executeQuery("SELECT * FROM ceseventhresult WHERE rollNo='"+rollNo+"' AND year='"+4+"' AND semester='"+1+"'");
            }
            else if(year ==4 && sem == 2){
                rs = st.executeQuery("SELECT * FROM ceeighthresult WHERE rollNo='"+rollNo+"' AND year='"+4+"' AND semester='"+2+"'");
            }
            
            if(!rs.first()){
                JOptionPane.showMessageDialog(null, "Entered RollNo. doesn't exist");
            }
            else{  
                setVisible(false);
                if(year ==1 && sem ==1){
                    StudentFirstSemResultPage frame = new StudentFirstSemResultPage(rollNo);
                    frame.setVisible(true);
                    frame.setLocationRelativeTo(null);
                }
                else if(year ==1 && sem ==2 ){
                    StudentSecondSemResultPage frame = new StudentSecondSemResultPage(rollNo);
                    frame.setVisible(true);
                    frame.setLocationRelativeTo(null);
                }
                else if(year ==2 && sem ==1 ){
                    CEThirdSemResultPage frame = new CEThirdSemResultPage(rollNo);
                    frame.setVisible(true);
                    frame.setLocationRelativeTo(null);
                }
                else if(year ==2 && sem ==2 ){
                    CEFourthSemResultPage frame = new CEFourthSemResultPage(rollNo);
                    frame.setVisible(true);
                    frame.setLocationRelativeTo(null);
                }
                else if(year ==3 && sem ==1 ){
                    CEFifthSemResultPage frame = new CEFifthSemResultPage(rollNo);
                    frame.setVisible(true);
                    frame.setLocationRelativeTo(null);
                }
                else if(year ==3 && sem ==2 ){
                    CESixthSemResultPage frame = new CESixthSemResultPage(rollNo);
                    frame.setVisible(true);
                    frame.setLocationRelativeTo(null);
                }
                else if(year ==4 && sem ==1 ){
                    CESeventhSemResultPage frame = new CESeventhSemResultPage(rollNo);
                    frame.setVisible(true);
                    frame.setLocationRelativeTo(null);
                }
                else if(year ==4 && sem ==2 ){
                    CEEighthSemResultPage frame = new CEEighthSemResultPage(rollNo);
                    frame.setVisible(true);
                    frame.setLocationRelativeTo(null);
                }
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_submit_ButtonActionPerformed

