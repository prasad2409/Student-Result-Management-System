    private void Search_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Search_ButtonActionPerformed
        // TODO add your handling code here:
        String rollNo = rollNo_TextField.getText();
        try{
            Connection con = ConnectionProviderCode.getcon();
            Statement st = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
            ResultSet rs = st.executeQuery("SELECT * FROM cesixthresult WHERE rollNo='"+rollNo+"'");

            if(!rs.first()){
                JOptionPane.showMessageDialog(null, "Entered RollNo. doesn't exist");
            }
            else{
                rollNo_TextField.setEditable(false);
                ddss_TextField.setText(rs.getString(4));
                ge1_TextField.setText(rs.getString(5));
                ee1_TextField.setText(rs.getString(6));
                wre1_TextField.setText(rs.getString(7));
                aes_TextField.setText(rs.getString(8));
                geLab_TextField.setText(rs.getString(9));
                eeLab_TextField1.setText(rs.getString(10));
                cadLab_TextField.setText(rs.getString(11));

            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_Search_ButtonActionPerformed

    private void update_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_update_ButtonActionPerformed
        // TODO add your handling code here:
        String rollNo = rollNo_TextField.getText();
        int ddsMarks = Integer.parseInt(ddss_TextField.getText());
        int ge1Marks = Integer.parseInt(ge1_TextField.getText());
        int ee1Marks = Integer.parseInt(ee1_TextField.getText());
        int wre1Marks = Integer.parseInt(wre1_TextField.getText());
        int aesMarks = Integer.parseInt(aes_TextField.getText());
        int gwLabMarks = Integer.parseInt(geLab_TextField.getText());
        int eeLabMarks = Integer.parseInt(eeLab_TextField1.getText());
        int cadLabMarks = Integer.parseInt(cadLab_TextField.getText());
        int total =ddsMarks+ge1Marks+ee1Marks+wre1Marks+aesMarks+gwLabMarks+eeLabMarks+ cadLabMarks;

        try{
            Connection con = ConnectionProviderCode.getcon();
            Statement st = con.createStatement();
            st.executeUpdate("UPDATE cesixthresult SET DDSS="+ddsMarks+", GE1="+ge1Marks+", EE1="+ee1Marks+", WRE1="+wre1Marks+", AE="+aesMarks+", GELab="+ gwLabMarks+", EELab="+eeLabMarks+", CADLab="+cadLabMarks+", total="+total+" WHERE rollNo='"+rollNo+"'");
            JOptionPane.showMessageDialog(null, "Updated Successfully");
            setVisible(false);
            UpdateCESixthseme frame = new UpdateCESixthseme();
            frame.setVisible(true);
            frame.setLocationRelativeTo(null);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_update_ButtonActionPerformed

