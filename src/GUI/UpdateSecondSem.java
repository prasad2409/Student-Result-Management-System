    private void search_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_search_ButtonActionPerformed
        // TODO add your handling code here:
        String rollNo = rollNo_TextField.getText();
        try{
            Connection con = ConnectionProviderCode.getcon();
            Statement st = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
            ResultSet rs = st.executeQuery("SELECT * FROM secondresult WHERE rollNo='"+rollNo+"'");
            
            if(!rs.first()){
                JOptionPane.showMessageDialog(null, "Entered RollNo. doesn't exist");
            }
            else{  
                rollNo_TextField.setEditable(false);
                englih2_TextField.setText(rs.getString(4));
                maths2_TextField.setText(rs.getString(5));
                maths3_TextField.setText(rs.getString(6));
                physics_TextField.setText(rs.getString(7));
                drawing_TextField.setText(rs.getString(8));
                phyLab_TextField.setText(rs.getString(9));
                ITLab_TextField.setText(rs.getString(10));
                
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_search_ButtonActionPerformed

    private void update_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_update_ButtonActionPerformed
        // TODO add your handling code here:
        String rollNo = rollNo_TextField.getText();
        int english2Marks = Integer.parseInt(englih2_TextField.getText());
        int maths2Marks = Integer.parseInt(maths2_TextField.getText());
        int maths3Marks = Integer.parseInt(maths3_TextField.getText());
        int physicsMarks = Integer.parseInt(physics_TextField.getText());
        int drawingMarks = Integer.parseInt(drawing_TextField.getText());
        int phyLabMarks = Integer.parseInt(phyLab_TextField.getText());
        int ITLabMarks = Integer.parseInt(ITLab_TextField.getText());
        int total = english2Marks + maths2Marks + maths3Marks + physicsMarks + drawingMarks+phyLabMarks+ITLabMarks;
        
        try{
            Connection con = ConnectionProviderCode.getcon();
            Statement st = con.createStatement();
            st.executeUpdate("UPDATE secondresult SET english2="+english2Marks+", maths2="+maths2Marks+", maths3="+maths3Marks+", physics="+physicsMarks+", drawing="+drawingMarks+", physicsLab="+ phyLabMarks+", ITWorkshop="+ITLabMarks+", total="+total+" WHERE rollNo='"+rollNo+"'");
            JOptionPane.showMessageDialog(null, "Updated Successfully");
            setVisible(false);
            UpdateSecondSem frame = new UpdateSecondSem();
            frame.setVisible(true);
            frame.setLocationRelativeTo(null);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_update_ButtonActionPerformed

