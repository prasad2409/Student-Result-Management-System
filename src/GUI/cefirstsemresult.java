    private void submit_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submit_ButtonActionPerformed
        // TODO add your handling code here:
        String rollNo = rollNo_TextField.getText();
        
        int english = Integer.parseInt(english_TextField.getText());
        int maths = Integer.parseInt(maths_TextField.getText());
        int che = Integer.parseInt(chem_TextField.getText());
        int mech = Integer.parseInt(mech_TextField.getText());
        int cp = Integer.parseInt(cp_TextField.getText());
        int chemLab = Integer.parseInt(chemLab_TextField.getText());
        int cpLab = Integer.parseInt(cpLab_TextField.getText());
        int total = english+maths+che+mech+cp+chemLab+cpLab;
        
        try{
            Connection con = ConnectionProviderCode.getcon();
            Statement st = con.createStatement();
            st.executeUpdate("insert into firstresult values("+ rollNo + ", " + 1 +", "+ 1 +", "+ english +", "+ maths+", "+ che + ", " + mech +", "+ cp+", "+ chemLab+", "+ cpLab+", "+total+")" );
            JOptionPane.showMessageDialog(null, "Added Successfully");
            setVisible(false);
            cefirstsemresult frame = new cefirstsemresult();
            frame.setVisible(true);
            frame.setLocationRelativeTo(null);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_submit_ButtonActionPerformed

