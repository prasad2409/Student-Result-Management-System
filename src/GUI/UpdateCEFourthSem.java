    private void search_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_search_ButtonActionPerformed
        // TODO add your handling code here:
        String rollNo = rollNo_TextField.getText();
        try{
            Connection con = ConnectionProviderCode.getcon();
            Statement st = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
            ResultSet rs = st.executeQuery("SELECT * FROM cefourthresult WHERE rollNo='"+rollNo+"'");
            
            if(!rs.first()){
                JOptionPane.showMessageDialog(null, "Entered RollNo. doesn't exist");
            }
            else{  
                rollNo_TextField.setEditable(false);
                bpd_TextField.setText(rs.getString(4));
                sm2_TextField.setText(rs.getString(5));
                hhm_TextField.setText(rs.getString(6));
                ct_TextField.setText(rs.getString(7));
                sa1_TextField.setText(rs.getString(8));
                te1_TextField.setText(rs.getString(9));
                fmhmLab_TextField1.setText(rs.getString(10));
                survLab_TextField.setText(rs.getString(11));
                
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_search_ButtonActionPerformed

    private void save_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_save_ButtonActionPerformed
        // TODO add your handling code here:
        String rollNo = rollNo_TextField.getText();
        int bpdMarks = Integer.parseInt(bpd_TextField.getText());
        int sm2Marks = Integer.parseInt(sm2_TextField.getText());
        int hhmMarks = Integer.parseInt(hhm_TextField.getText());
        int ctMarks = Integer.parseInt(ct_TextField.getText());
        int sa1Marks = Integer.parseInt(sa1_TextField.getText());
        int te1Marks = Integer.parseInt(te1_TextField.getText());
        int fmhmLabMarks = Integer.parseInt(fmhmLab_TextField1.getText());
        int surveyLab2Marks = Integer.parseInt(survLab_TextField.getText());
        int total =bpdMarks+sm2Marks+hhmMarks+ctMarks+sa1Marks+te1Marks+fmhmLabMarks+surveyLab2Marks ;
        
        try{
            Connection con = ConnectionProviderCode.getcon();
            Statement st = con.createStatement();
            st.executeUpdate("UPDATE cefourthresult SET BPD="+bpdMarks+", SM2="+sm2Marks+", HHM="+hhmMarks+", CT="+ctMarks+", SA="+sa1Marks+", TE1="+ te1Marks+", FMHMLab="+fmhmLabMarks+", surveyLab2="+surveyLab2Marks+", total="+total+" WHERE rollNo='"+rollNo+"'");
            JOptionPane.showMessageDialog(null, "Updated Successfully");
            setVisible(false);
            UpdateCEFourthSem frame = new UpdateCEFourthSem();
            frame.setVisible(true);
            frame.setLocationRelativeTo(null);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_save_ButtonActionPerformed

