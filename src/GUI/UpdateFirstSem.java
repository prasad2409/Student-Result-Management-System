    private void search_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_search_ButtonActionPerformed
        // TODO add your handling code here:
        String rollNo = rollNo_TextField.getText();
        try{
            Connection con = ConnectionProviderCode.getcon();
            Statement st = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
            ResultSet rs = st.executeQuery("SELECT * FROM firstresult WHERE rollNo='"+rollNo+"'");
            
            if(!rs.first()){
                JOptionPane.showMessageDialog(null, "Entered RollNo. doesn't exist");
            }
            else{  
                rollNo_TextField.setEditable(false);
                englih1_TextField.setText(rs.getString(4));
                maths_TextField.setText(rs.getString(5));
                chem_TextField.setText(rs.getString(6));
                mech_TextField.setText(rs.getString(7));
                cp_TextField.setText(rs.getString(8));
                chemLab_TextField.setText(rs.getString(9));
                cpLab_TextField.setText(rs.getString(10));
                
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_search_ButtonActionPerformed

    private void save_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_save_ButtonActionPerformed
        // TODO add your handling code here:
        String rollNo = rollNo_TextField.getText();
        int english1Marks = Integer.parseInt(englih1_TextField.getText());
        int maths1Marks = Integer.parseInt(maths_TextField.getText());
        int chemMarks = Integer.parseInt(chem_TextField.getText());
        int mechMarks = Integer.parseInt(mech_TextField.getText());
        int cpMarks = Integer.parseInt(cp_TextField.getText());
        int chemLabMarks = Integer.parseInt(chemLab_TextField.getText());
        int cpLabMarks = Integer.parseInt(cpLab_TextField.getText());
        int total = english1Marks + maths1Marks + chemMarks + mechMarks + cpMarks+chemLabMarks+cpLabMarks;
        
        try{
            Connection con = ConnectionProviderCode.getcon();
            Statement st = con.createStatement();
            st.executeUpdate("UPDATE firstresult SET english1="+english1Marks+", maths1="+maths1Marks+", chemistry="+chemMarks+", mechanics="+mechMarks+", cp="+cpMarks+", chemistryLab="+ chemLabMarks+", cpLab="+cpLabMarks+", total="+total+" WHERE rollNo='"+rollNo+"'");
            JOptionPane.showMessageDialog(null, "Updated Successfully");
            setVisible(false);
            UpdateFirstSem frame = new UpdateFirstSem();
            frame.setVisible(true);
            frame.setLocationRelativeTo(null);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_save_ButtonActionPerformed

