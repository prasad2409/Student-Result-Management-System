    private void search_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_search_ButtonActionPerformed
        // TODO add your handling code here:
        String rollNo = rollNo_TextField.getText();
        try{
            Connection con = ConnectionProviderCode.getcon();
            Statement st = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
            ResultSet rs = st.executeQuery("SELECT * FROM ceeighthresult WHERE rollNo='"+rollNo+"'");

            if(!rs.first()){
                JOptionPane.showMessageDialog(null, "Entered RollNo. doesn't exist");
            }
            else{
                rollNo_TextField.setEditable(false);
                esc_TextField.setText(rs.getString(4));
                ctm_TextField.setText(rs.getString(5));
                pc_TextField.setText(rs.getString(6));
                shwm_TextField.setText(rs.getString(7));
                seminar_TextField.setText(rs.getString(8));
                project_TextField.setText(rs.getString(9));
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_search_ButtonActionPerformed

    private void update_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_update_ButtonActionPerformed
        // TODO add your handling code here:
        String rollNo = rollNo_TextField.getText();
        int escMarks = Integer.parseInt(esc_TextField.getText());
        int ctmMarks = Integer.parseInt(ctm_TextField.getText());
        int pcMarks = Integer.parseInt(pc_TextField.getText());
        int shwmMarks = Integer.parseInt(shwm_TextField.getText());
        int seminarMarks = Integer.parseInt(seminar_TextField.getText());
        int projectMarks = Integer.parseInt(project_TextField.getText());
        
        int total = escMarks + ctmMarks + pcMarks + shwmMarks + seminarMarks + projectMarks;

        try{
            Connection con = ConnectionProviderCode.getcon();
            Statement st = con.createStatement();
            st.executeUpdate("UPDATE ceeighthresult SET ESC="+escMarks+", CTM="+ctmMarks+", PC="+pcMarks+",  SHWM="+shwmMarks+", seminar="+seminarMarks+", project="+ projectMarks+", total="+total+" WHERE rollNo='"+rollNo+"'");
            JOptionPane.showMessageDialog(null, "Updated Successfully");
            setVisible(false);
            UpdateCEEighthSem frame = new UpdateCEEighthSem();
            frame.setVisible(true);
            frame.setLocationRelativeTo(null);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_update_ButtonActionPerformed

