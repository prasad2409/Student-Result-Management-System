    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        String rollNo = rollNo_TextField1.getText();
        try{
            Connection con = ConnectionProviderCode.getcon();
            Statement st = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);  
            ResultSet rs = st.executeQuery("select * from student inner join firstresult where student.rollNo='"+rollNo+"' and firstresult.rollNo='"+rollNo+"' and student.year='"+1+"' and firstresult.year='"+1+"' and student.semester='"+1+"' and firstresult.semester='"+1+"'");
            
            if(!rs.first()){
                JOptionPane.showMessageDialog(null, "Entered RollNo. doesn't exist");
            }
            else{ 
                name_TextField.setText(rs.getString(1));
                gender_TextField.setText(rs.getString(3));
                fatherName_TextField.setText(rs.getString(4));
                course_TextField.setText(rs.getString(5));
                branch_TextField.setText(rs.getString(6));
                
                english1_TextField.setText(rs.getString(12));
                maths_TextField.setText(rs.getString(13));
                chem_TextField.setText(rs.getString(14));
                mech_TextField.setText(rs.getString(15));
                cp_TextField.setText(rs.getString(16));
                chemLab_TextField.setText(rs.getString(17));
                pro_TextField.setText(rs.getString(18));
                total_TextField.setText(rs.getString(19));
                
                rollNo_TextField1.setEditable(false);
                name_TextField.setEditable(false);
                gender_TextField.setEditable(false);
                fatherName_TextField.setEditable(false);
                course_TextField.setEditable(false);
                branch_TextField.setEditable(false);
                
                english1_TextField.setEditable(false);
                maths_TextField.setEditable(false);
                chem_TextField.setEditable(false);
                mech_TextField.setEditable(false);
                cp_TextField.setEditable(false);
                chemLab_TextField.setEditable(false);
                pro_TextField.setEditable(false);
                total_TextField.setEditable(false);
                
                int english   = rs.getInt(12);
                int mathsMarks =rs.getInt(13);
                int chem   = rs.getInt(14);
                int mecha =rs.getInt(15);
                int cp   = rs.getInt(16);
                int chemLab =rs.getInt(17);
                int prograLab =rs.getInt(18);
           
                
                
                if(english>=40 && mathsMarks>=40 && chem>=40 && mecha >=40 && cp >=40 && chemLab >=40 && prograLab >=40){
                    verdict_TextField.setText("PASS");
                }
                else{
                    verdict_TextField.setText("FAIL");
                }
                verdict_TextField.setEditable(false);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_formComponentShown

