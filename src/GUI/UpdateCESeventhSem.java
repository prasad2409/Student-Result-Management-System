    private void search_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_search_ButtonActionPerformed
        // TODO add your handling code here:
        String rollNo = rollNo_TextField.getText();
        try{
            Connection con = ConnectionProviderCode.getcon();
            Statement st = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
            ResultSet rs = st.executeQuery("SELECT * FROM ceseventhresult WHERE rollNo='"+rollNo+"'");

            if(!rs.first()){
                JOptionPane.showMessageDialog(null, "Entered RollNo. doesn't exist");
            }
            else{
                rollNo_TextField.setEditable(false);
                ee2_TextField.setText(rs.getString(4));
                wre2_TextField.setText(rs.getString(5));
                ge2_TextField.setText(rs.getString(6));
                gis_TextField.setText(rs.getString(7));
                git_TextField.setText(rs.getString(8));
                gwd_TextField.setText(rs.getString(9));
                GISLab_TextField1.setText(rs.getString(10));
                iddLab_TextField.setText(rs.getString(11));

            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_search_ButtonActionPerformed

    private void updste_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updste_ButtonActionPerformed
        // TODO add your handling code here:
        String rollNo = rollNo_TextField.getText();
        int ee2Marks = Integer.parseInt(ee2_TextField.getText());
        int wre2Marks = Integer.parseInt(wre2_TextField.getText());
        int ge2Marks = Integer.parseInt(ge2_TextField.getText());
        int gisMarks = Integer.parseInt(gis_TextField.getText());
        int gitMarks = Integer.parseInt(git_TextField.getText());
        int gwdMarks = Integer.parseInt(gwd_TextField.getText());
        int GISLabMarks = Integer.parseInt(GISLab_TextField1.getText());
        int iddLabMarks = Integer.parseInt(iddLab_TextField.getText());
        int total =ee2Marks + wre2Marks + ge2Marks + gisMarks + gitMarks + gwdMarks + GISLabMarks + iddLabMarks;

        try{
            Connection con = ConnectionProviderCode.getcon();
            Statement st = con.createStatement();
            st.executeUpdate("UPDATE ceseventhresult SET EE2="+ee2Marks+", WRE2="+wre2Marks+", GE2="+ge2Marks+",  RSANDGIS="+gisMarks+", GIP="+gitMarks+", GWD="+ gwdMarks+",GISLAB ="+GISLabMarks+",STADLab ="+iddLabMarks+", total="+total+" WHERE rollNo='"+rollNo+"'");
            JOptionPane.showMessageDialog(null, "Updated Successfully");
            setVisible(false);
            UpdateCESeventhSem frame = new UpdateCESeventhSem();
            frame.setVisible(true);
            frame.setLocationRelativeTo(null);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_updste_ButtonActionPerformed

