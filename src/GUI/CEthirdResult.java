    private void submit_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submit_ButtonActionPerformed
        // TODO add your handling code here:
        String rollNo = rollNo_TextField.getText();

        int ps = Integer.parseInt(probandsta_TextField.getText());
        int sm1 = Integer.parseInt(sm1_TextField.getText());
        int bmc = Integer.parseInt(bmc_TextField.getText());
        int survey = Integer.parseInt(surveying_TextField.getText());
        int fm = Integer.parseInt(fm_TextField.getText());
        int surveyLab = Integer.parseInt(surveyLab_TextField.getText());
        int smLab = Integer.parseInt(smLab_TextField.getText());
        int total = ps+sm1+bmc+survey+fm+surveyLab+smLab;

        try{
            Connection con = ConnectionProviderCode.getcon();
            Statement st = con.createStatement();
            st.executeUpdate("insert into cethirdresult values("+ rollNo + ", " + 2 +", "+ 1 +", "+ ps +", "+ sm1+", "+ bmc + ", " + survey +", "+ fm+", "+ surveyLab+", "+ smLab+", "+total+")" );
            JOptionPane.showMessageDialog(null, "Added Successfully");
            setVisible(false);
            CEthirdResult frame = new CEthirdResult();
            frame.setVisible(true);
            frame.setLocationRelativeTo(null);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_submit_ButtonActionPerformed

