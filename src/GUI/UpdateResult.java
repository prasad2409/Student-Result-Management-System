    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        // TODO add your handling code here:
        String rollNo = rollNo_TextField.getText();
        try{
            Connection con = ConnectionProviderCode.getcon();
            Statement st = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
            ResultSet rs = st.executeQuery("SELECT * FROM result WHERE rollNo='"+rollNo+"'");
            
            if(!rs.first()){
                JOptionPane.showMessageDialog(null, "Entered RollNo. doesn't exist");
            }
            else{  
                rollNo_TextField.setEditable(false);
                physicsField.setText(rs.getString(2));
                mathsField.setText(rs.getString(3));
                chemistryField.setText(rs.getString(4));
                electricalField.setText(rs.getString(5));
                biologyField.setText(rs.getString(6));
                
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_searchButtonActionPerformed

    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        String rollNo = rollNo_TextField.getText();
        int physicsMarks = Integer.parseInt(physicsField.getText());
        int mathsMarks = Integer.parseInt(mathsField.getText());
        int chemMarks = Integer.parseInt(chemistryField.getText());
        int electricalMarks = Integer.parseInt(electricalField.getText());
        int bioMarks = Integer.parseInt(biologyField.getText());
        int total = physicsMarks + mathsMarks + chemMarks + electricalMarks + bioMarks;
        
        try{
            Connection con = ConnectionProviderCode.getcon();
            Statement st = con.createStatement();
            st.executeUpdate("UPDATE result SET physics="+physicsMarks+", maths="+mathsMarks+", chemistry="+chemMarks+", electrical="+electricalMarks+", biology="+bioMarks+", total="+total+" WHERE rollNo='"+rollNo+"'");
            JOptionPane.showMessageDialog(null, "Added Successfully");
            setVisible(false);
            UpdateResult frame = new UpdateResult();
            frame.setVisible(true);
            frame.setLocationRelativeTo(null);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_saveButtonActionPerformed

