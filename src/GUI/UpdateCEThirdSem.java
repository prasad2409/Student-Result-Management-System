    private void search_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_search_ButtonActionPerformed
        // TODO add your handling code here:
        String rollNo = rollNo_TextField.getText();
        try{
            Connection con = ConnectionProviderCode.getcon();
            Statement st = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
            ResultSet rs = st.executeQuery("SELECT * FROM cethirdresult WHERE rollNo='"+rollNo+"'");
            
            if(!rs.first()){
                JOptionPane.showMessageDialog(null, "Entered RollNo. doesn't exist");
            }
            else{  
                rollNo_TextField.setEditable(false);
                ps_TextField.setText(rs.getString(4));
                sm1_TextField.setText(rs.getString(5));
                bmc_TextField.setText(rs.getString(6));
                surveying_TextField.setText(rs.getString(7));
                fm_TextField.setText(rs.getString(8));
                surveyLab_TextField.setText(rs.getString(9));
                smLab_TextField.setText(rs.getString(10));
                
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_search_ButtonActionPerformed

    private void save_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_save_ButtonActionPerformed
        // TODO add your handling code here:
        String rollNo = rollNo_TextField.getText();
        int psMarks = Integer.parseInt(ps_TextField.getText());
        int sm1Marks = Integer.parseInt(sm1_TextField.getText());
        int bmcMarks = Integer.parseInt(bmc_TextField.getText());
        int surveyMarks = Integer.parseInt(surveying_TextField.getText());
        int fluidMarks = Integer.parseInt(fm_TextField.getText());
        int surveyLabMarks = Integer.parseInt(surveyLab_TextField.getText());
        int smLabMarks = Integer.parseInt(smLab_TextField.getText());
        int total = psMarks + sm1Marks + bmcMarks + surveyMarks + fluidMarks+surveyLabMarks+smLabMarks;
        
        try{
            Connection con = ConnectionProviderCode.getcon();
            Statement st = con.createStatement();
            st.executeUpdate("UPDATE cethirdresult SET probability="+psMarks+", SM1="+sm1Marks+", BMC="+bmcMarks+", surveying="+surveyMarks+", fluidmech="+fluidMarks+", surveyLab="+ surveyLabMarks+", SMLab="+smLabMarks+", total="+total+" WHERE rollNo='"+rollNo+"'");
            JOptionPane.showMessageDialog(null, "Updated Successfully");
            setVisible(false);
            UpdateCEThirdSem frame = new UpdateCEThirdSem();
            frame.setVisible(true);
            frame.setLocationRelativeTo(null);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_save_ButtonActionPerformed

