    private void submit_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submit_ButtonActionPerformed
        // TODO add your handling code here:
        String rollNo = rollNo_TextField.getText();
        
        int english = Integer.parseInt(englishII_TextField.getText());
        int maths2 = Integer.parseInt(mathsII_TextField.getText());
        int maths3 = Integer.parseInt(mathsIII_TextField.getText());
        int phy = Integer.parseInt(phy_TextField.getText());
        int drawing = Integer.parseInt(drawing_TextField.getText());
        int phyLab = Integer.parseInt(phyLab_TextField.getText());
        int ITLab = Integer.parseInt(ITWork_TextField.getText());
        int total = english+maths2+maths3+phy+drawing+phyLab+ITLab;
        
        try{
            Connection con = ConnectionProviderCode.getcon();
            Statement st = con.createStatement();
            st.executeUpdate("insert into secondresult values("+ rollNo + ", " + 1 +", "+ 2 +", "+ english +", "+ maths2+", "+ maths3 + ", " + phy +", "+ drawing+", "+ phyLab+", "+ ITLab+", "+total+")" );
            JOptionPane.showMessageDialog(null, "Added Successfully");
            setVisible(false);
            SecondResult frame = new SecondResult();
            frame.setVisible(true);
            frame.setLocationRelativeTo(null);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_submit_ButtonActionPerformed

