    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        String rollNo = rollNoField.getText();
        try{
            Connection con = ConnectionProviderCode.getcon();
            Statement st = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);  
            ResultSet rs = st.executeQuery("select * from student inner join result where student.rollNo='"+rollNo+"' and result.rollNo='"+rollNo+"'");
            
            if(!rs.first()){
                JOptionPane.showMessageDialog(null, "Entered RollNo. doesn't exist");
            }
            else{ 
                nameField.setText(rs.getString(1));
                genderField.setText(rs.getString(3));
                fatherNameField.setText(rs.getString(4));
                courseField.setText(rs.getString(5));
                branchField.setText(rs.getString(6));
                
                physicsField.setText(rs.getString(8));
                mathsField.setText(rs.getString(9));
                chemistryField.setText(rs.getString(10));
                electricalField.setText(rs.getString(11));
                biologyField.setText(rs.getString(12));
                totalField.setText(rs.getString(13));
                
                rollNoField.setEditable(false);
                nameField.setEditable(false);
                genderField.setEditable(false);
                fatherNameField.setEditable(false);
                courseField.setEditable(false);
                branchField.setEditable(false);
                
                physicsField.setEditable(false);
                mathsField.setEditable(false);
                chemistryField.setEditable(false);
                electricalField.setEditable(false);
                biologyField.setEditable(false);
                totalField.setEditable(false);
                
                int physicsMarks = Integer.parseInt(rs.getString(8));
                int mathsMarks = Integer.parseInt(rs.getString(9));
                int chemistryMarks = Integer.parseInt(rs.getString(10));
                int electricalsMarks = Integer.parseInt(rs.getString(11));
                int biologyMarks = Integer.parseInt(rs.getString(12));
                
                if(physicsMarks>=30 && mathsMarks>=30 && chemistryMarks>=30 && electricalsMarks >=30 && biologyMarks >=30){
                    verdictField.setText("PASS");
                }
                else{
                    verdictField.setText("FAIL");
                }
                verdictField.setEditable(false);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_formComponentShown

